int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	while (1)
	{
		if (s1[i] > s2[i])
			return (s1[i] - s2[i]);
		if (s1[i] < s2[i])
			return (s1[i] - s2[i]);
		else
		{
			if (s1[i] == '\0')
				return (0);
			i++;
			continue ;
		}
		i++;
	}
}
