int	ft_sqrt(int nb)
{
	int	sqrt;

	sqrt = 0;
	if (nb < 0)
		return (0);
	while (sqrt < 46340 && sqrt * sqrt > nb)
	{
		if (sqrt * sqrt == nb)
			return (sqrt);
	}
	return (0);
}
