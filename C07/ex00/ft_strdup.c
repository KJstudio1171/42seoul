#include <stdlib.h>

char	*ft_strdup(char *src)
{
	int		len;
	int		i;
	char	*result;

	len = 0;
	i = 0;
	while (src[len])
		len++;
	result = (char *)malloc(sizeof(char) * (len + 1));
	if (!(result))
		return (NULL);
	while (src[i])
	{
		result[i] = src[i];
		i++;
	}
	result[i] = '\0';
	return (result);
}
