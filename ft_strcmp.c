int strcmp(char *s1,char *s2)
{
	int i;
	while(1)
	{
		if(s1[i] > s2[i])
			return 1;
		if(s1[i] < s2[i])
			return -1;
		else
		{
			if(s1[i]=='\0')
				return 0;
			i++;
			continue;
		}
		i++;
	}
}